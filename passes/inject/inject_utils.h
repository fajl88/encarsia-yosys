#ifndef INJECT_UTILS_H
#define INJECT_UTILS_H

#include <string>

static inline std::string json_escape(const std::string &input)
{
	std::string escaped;
	escaped.reserve(input.size());
	for (char c : input) {
		switch (c) {
		case '\\': escaped += "\\\\"; break;
		case '"': escaped += "\\\""; break;
		case '\n': escaped += "\\n"; break;
		case '\r': escaped += "\\r"; break;
		case '\t': escaped += "\\t"; break;
		default: escaped += c; break;
		}
	}
	return escaped;
}

#endif
